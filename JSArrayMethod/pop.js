// pop() method removes the last element of an array. and return the removed element. And this method changes the original array.

// syntax -  array.pop()

let bikes = ["Duke 250", "pulsar 220F", "Meteor 350", "FZ"];
console.log(bikes.pop());
console.log(bikes); //[ 'Duke 250', 'pulsar 220F', 'Meteor 350' ]
console.log(bikes.pop(2)); // Meteor 350
