// A volume of a rectangular prism is calculated as
// follows: volume = length x width x height.Write a function which calculates volumeOfRectPrism.



function volumeOfRectPrism(length, width, height) {
    let volume = length * width * height;

    console.log(`Volume of rectangular prism is ${volume}`);
}
volumeOfRectPrism(10, 5, 20);  //1000